var checkkm = $('#checkkm');
var checkao = $('#checkao');
var checkquan = $('#checkquan');
var first = true;


checkkm.on('click', filterkm)
checkao.on('click', filterao);
checkquan.on('click', filterquan);
function filterkm() {
	if(first){
		quanphuot.html('');
		aophuot.html('');
		first = false;
	}
	if(!checkkm.hasClass('tick')){	
		checkkm.addClass('tick');
		showkm();
	} else {		
		checkkm.removeClass('tick');
		km.html('');
	};
	console.log('a')
}


